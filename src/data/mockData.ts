// Get restaurant by ID
export function getRestaurantById(id: string): Restaurant | undefined {
  return restaurants.find((r) => r.id === id);
}

// Get food items by restaurant ID
export function getFoodItemsByRestaurantId(restaurantId: string): FoodItem[] {
  return foodItems.filter((item) => item.restaurantId === restaurantId);
}

// Get all unique categories from food items
export function getAllFoodCategories(): string[] {
  const categories = new Set(foodItems.map((item) => item.category));
  return Array.from(categories);
}
